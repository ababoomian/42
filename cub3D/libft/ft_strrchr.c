#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = ft_strlen(s);
	if (c == '\0')
		return ((char *)s + i);
	while (s[--i])
		if ((unsigned char)s[i] == (unsigned char)c)
			return ((char *)s + i);
	return (0);
}
